    var KTTOrderHandler = function() {
        var createOrder = function() {
            $('#createOrder').on('click', function(e) {
                e.preventDefault();

                var loading = new KTDialog({
                        'type': 'loader',
                        'placement': 'top center',
                        'message': 'Loading ...'
                    }),
                    error = 'Undefined Error',
                    planPrice = $('#orderPrice').text(),
                    discountCode = $('#applyDiscount').val(),
                    paymentMethod = $('#paymentMethod').val();
                var selectedPlan = $('#selectedPlan').val(),
                    splitedPlans = selectedPlan.split("|");

                $('#orderPrice').empty().text("$" + splitedPlans[1]);

                $(this).ajaxSubmit({
                    url: '/rest/user/orders/discount',
                    data: {
                        'base_price': planPrice,
                        'discount_code': discountCode
                    },
                    type: 'POST',
                    error: function(response, status, xhr, $form) {
                        $('#confirmPaymentModal').modal('show');
                        loading.hide();
                    },
                    success: function(response, status, xhr, $form) {
                        if (response.Status) {
                            if (response.Discount.applied) {
                                swal.fire({
                                    "title": "",
                                    "html": "Discount Applied ( - $" + response
                                        .Discount.applied + ")",
                                    "type": "success",
                                    "confirmButtonClass": "btn btn-secondary"
                                });

                                $('#orderPrice').empty().text("$" + response.Discount
                                    .new_price);
                            }
                        }

                        $('#createOrder').ajaxSubmit({
                            url: '/rest/user/orders/prepare',
                            data: {
                                'payment_method': paymentMethod,
                                'order_price': (response.Status) ? response.Discount
                                    .new_price : planPrice,
                                'plan_id': splitedPlans[0]
                            },
                            type: 'POST',
                            error: function(response, status, xhr, $form) {
                                swal.fire({
                                    "title": "",
                                    "html": "Something Went Wrong, Please try again later!",
                                    "type": "error",
                                    "confirmButtonClass": "btn btn-secondary"
                                });
                            },
                            success: function(response, status, xhr, $form) {
                                if (response.Status) {
                                    if (paymentMethod === 'paypal') {
                                        $('#paypalPrice').text($('#orderPrice')
                                            .text());
                                        $('#paypalpay').val(response
                                            .ForwardUrl);
                                        $('#confirm-paypal-payment').modal(
                                            'show');
                                    } else {
                                        $('#confirmPaymentModal').find(
                                            '.kt-widget13').append(
                                            '<div class="kt-widget13__item">\n' +
                                            '  <span class="kt-widget13__desc">Receiving Address</span>\n' +
                                            '  <span id="orderPaymentMethod" class="kt-widget13__text kt-widget13__text--bold">' +
                                            response.Address + '</span>\n' +
                                            '</div>\n' +
                                            '<div class="kt-widget13__item">\n' +
                                            '<img style="margin:0 auto;" src="https://chart.googleapis.com/chart?chs=250x250&cht=qr&chl=' +
                                            response.Address + '" />\n' +
                                            '</div>'
                                        );
                                        $('#txId').val(response.Address);
                                        $('#confirmPaymentModal').modal('show');
                                    }

                                } else {
                                    swal.fire({
                                        "title": "",
                                        "html": "Something Went Wrong, Please try again later!",
                                        "type": "error",
                                        "confirmButtonClass": "btn btn-secondary"
                                    });
                                }
                            }
                        });
                        loading.hide();
                    }
                });
            });
            $('#selectedPlan').on('change', function(e) {
                e.preventDefault();

                var selectedPlan = $(this).val(),
                    splitedPlans = selectedPlan.split("|");

                $('#orderPrice').empty().text("$" + splitedPlans[1]);
            });
            $('#paymentMethod').on('change', function(e) {
                e.preventDefault();

                var paymentMethod = $(this).val(),
                    paymentName = paymentMethod == 'paypal' ? 'Paypal' : 'Bitcoin';

                $('#orderPaymentMethod').empty().text(paymentName);
            });
            $('#confirmCreateOrder').on('click', function(e) {
                e.preventDefault();

                var loading = new KTDialog({
                        'type': 'loader',
                        'placement': 'top center',
                        'message': 'Loading ...'
                    }),
                    error = 'Undefined Error',
                    orderForm = $('#createOrderForm'),
                    orderConfirmModal = $('#confirmPaymentModal');

                loading.show();
                orderForm.ajaxSubmit({
                    url: '/rest/user/orders/create',
                    type: 'POST',
                    error: function(response, status, xhr, $form) {
                        if (response.responseJSON) {
                            error = response.responseJSON.error;
                        }

                        swal.fire({
                            "title": "",
                            "text": error,
                            "type": "error",
                            "buttonStyling": false,
                            "confirmButtonClass": "btn btn-brand btn-sm btn-bold"
                        });
                        loading.hide();
                    },
                    success: function(response, status, xhr, $form) {
                        if (response.Status) {
                            swal.fire({
                                "title": "",
                                "html": response.Message,
                                "type": "success",
                                "confirmButtonClass": "btn btn-secondary"
                            });
                            orderConfirmModal.modal('hide');
                            location.reload();
                        } else {
                            swal.fire({
                                "title": "",
                                "text": response.Message,
                                "type": "error",
                                "confirmButtonClass": "btn btn-secondary"
                            });
                        }
                        loading.hide();
                    }
                });
            });
            $('#confirmPaypalOrder').on('click', function(e) {
                window.location.href = $('#paypalpay').val();
            });
        };
        return {
            init: function() {
                createOrder();
            }

    var CreateProjectUsernameWizard = function() {
        // Base elements
        var wizardEl;
        var formEl;
        var validator;
        var wizard;

        // Private functions
        var initWizard = function() {
            // Initialize form wizard
            wizard = new KTWizard('kt_wizard_v3', {
                startStep: 1, // initial active step number
                clickableSteps: true // allow step clicking
            });

            // Validation before going to next page
            wizard.on('beforeNext', function(wizardObj) {
                if (validator.form() !== true) {
                    wizardObj.stop(); // don't go to the next step
                }

                if (wizardObj.getStep() == 2) {
                    $('#instaUsernameUScrapTarget').text($('#instaUsernameUScrap').val());

                    var scrapTypes = '',
                        totalScraps = 0;

                    $.each($('input[class=kt-switch_scraping_type-username]:checked'), function(i,
                        element) {
                        var dataElement = element.getAttribute('data-scrap'),
                            dataScrap = $('.total_scraps-' + dataElement).val();

                        scrapTypes += dataElement + '<br>';
                        totalScraps = totalScraps + parseInt(dataScrap);
                    });

                    $('#scrapTypesTarget').html(scrapTypes);
                    $('#totalCreditsTarget').text(totalScraps);
                    $('#taskMaxTarget').val(totalScraps);
                }
            });

            wizard.on('beforePrev', function(wizardObj) {
                if (validator.form() !== true) {
                    wizardObj.stop(); // don't go to the next step
                }
            });

            // Change event
            wizard.on('change', function(wizard) {
                KTUtil.scrollTop();
            });
        }

        var initValidation = function() {
            validator = formEl.validate({
                // Validate only visible fields
                ignore: ":hidden",

                // Validation rules
                rules: {
                    //= Step 1
                    task_destination: {
                        required: true
                    },

                    //= Step 2
                    username_followers: {
                        require_from_group: [1, ".kt-switch_scraping_type-username"]
                    },
                    username_followings: {
                        require_from_group: [1, ".kt-switch_scraping_type-username"]
                    },
                    username_likes: {
                        require_from_group: [1, ".kt-switch_scraping_type-username"]
                    },
                    username_posts: {
                        require_from_group: [1, ".kt-switch_scraping_type-username"]
                    },
                    username_comments: {
                        require_from_group: [1, ".kt-switch_scraping_type-username"]
                    },
                    task_name: {
                        required: true
                    }
                },


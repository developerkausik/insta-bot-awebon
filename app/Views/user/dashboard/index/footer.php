                        "type": "error",
                        "confirmButtonClass": "btn btn-secondary"
                    });
                },

                // Submit valid form
                submitHandler: function(form) {

                }
            });
        }

        var initSubmit = function() {
            var btn = formEl.find('[data-ktwizard-type="action-submit"]');

            btn.on('click', function(e) {
                e.preventDefault();
                if (validator.form()) {
                    KTApp.progress(btn);

                    formEl.ajaxSubmit({
                        success: function(response) {
                            KTApp.unprogress(btn);

                            if (response.Status) {
                                swal.fire({
                                    "title": "",
                                    "text": response.Message,
                                    "type": "success",
                                    "confirmButtonClass": "btn btn-secondary"
                                });
                                location.reload();
                            } else {
                                swal.fire({
                                    "title": "",
                                    "text": response.Message,
                                    "type": "error",
                                    "confirmButtonClass": "btn btn-secondary"
                                });
                            }
                        }
                    });
                }
            });
        }

        var autoComplete = function() {
            var users = new Bloodhound({
                datumTokenizer: Bloodhound.tokenizers.whitespace,
                queryTokenizer: Bloodhound.tokenizers.whitespace,
                remote: {
                    url: '/rest/user/task/autocomplete?entity=users',
                    prepare: function(query, settings) {
                        settings.url += '&query=' + query;
                        return settings;
                    },
                    filter: function(data) {
                        var userData = data.data;
                        return $.map(userData, function(obj) {
                            return {
                                username: obj.username,
                                id: obj.pk,
                                profile_pic: obj.profile_pic_url,
                                is_verified: obj.is_verified
                            };
                        });
                    }
                }
            });

            $('#instaUsernameUScrap')
                .typeahead(null, {
                    limit: 100,
                    name: 'users',
                    source: users,
                    templates: {
                        suggestion: function(data) {
                            var html =
                                '<div style="display:flex;padding: 7px 5px;border-bottom: 1px solid rgba(149,156,182,0.3);">';

                                html +=
                                    '<div style="margin-left: 15px;align-items: center;display: flex;justify-content: center;"><img src="<?= base_url(); ?>/assets/media/icons/verified.png" style="width: 15px;height:15px;vertical-align: middle;"/></div>';
                            }
                            html += '</div>';
                            return html;
                        },
                    }
                }).bind('typeahead:select', function(ev, suggestion) {
                    $('#instaUsernameUScrap').typeahead('val', suggestion.username);
                    $('#instaUsernameIdUScrap').val(suggestion.id);
                });
        }

